/**
 * Kotlin code is usually defined in packages. Package specification is optional: If you
 * don't specify a package in a source file, its content goes to the default package.
 */
package intro

/**
 * An entry point to a Kotlin application is the main function. In Kotlin 1.3, you can
 * declare main without any parameters. The return type is not specified, which means
 * that the function returns nothing.
 */
fun main() {

    /**
     * println writes a line to the standard output. It is imported implicitly.
     * Also note that semicolons are optional.
     */
    println("Hello World")
}