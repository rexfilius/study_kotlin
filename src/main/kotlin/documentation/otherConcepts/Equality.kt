package documentation.otherConcepts

/**
 * In Kotlin there are two types of equality:
 * 1. Structural equality; (==) a check for equals().
 * 2. Referential equality; (===) two references point to the same object
 */

/**
 * Structural equality is checked by the '==' operation and its negated counterpart '!='
 */

/**
 * Referential equality is checked by the '===' operation and its negated
 * counterpart '!=='... a === b evaluates to true if and only if a and b point to
 * the same object. For values represented by primitive types at runtime e.g. Int,
 * the '===' equality check is equivalent to the == check
 */